#mantıksal operatörler
#and operatörü belirtilen her iki ifade de doğru olduğunda True diğer tüm durumlarda False döndürür.

print(True and True)
print(True and False)
print(False and False)

#or operatörü belirtilen iki durumdan en az birinin doğru olduğu durumda True diğer tüm durumlarda False döndürür.

print(True or True)
print(False or True)
print(False or False)

#not operatörü sonucu tersine çevirir

print(not True)
print(not False)

